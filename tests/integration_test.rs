/// Test ConfidenceInterval construction and methods.
#[test]
fn test_confidence_interval() {
    let ci = ConfidenceInterval::new(90.0, 110.0, 0.95).unwrap();

    assert_eq!(ci.lower, 90.0);
    assert_eq!(ci.upper, 110.0);
    assert_eq!(ci.confidence, 0.95);
    assert_eq!(ci.width(), 20.0);
    assert_eq!(ci.midpoint(), 100.0);

    // Containment checks.
    assert!(ci.contains(100.0));
    assert!(ci.contains(90.0));
    assert!(ci.contains(110.0));
    assert!(!ci.contains(89.9));
    assert!(!ci.contains(110.1));
}

/// Test that invalid confidence intervals are rejected.
#[test]
fn test_confidence_interval_invalid() {
    // Inverted bounds.
    assert!(ConfidenceInterval::new(110.0, 90.0, 0.95).is_err());
    // Confidence out of range.
    assert!(ConfidenceInterval::new(0.0, 1.0, 0.0).is_err());
    assert!(ConfidenceInterval::new(0.0, 1.0, 1.0).is_err());
}

