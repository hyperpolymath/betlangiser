/// Test that `init_manifest` creates a valid betlangiser.toml file.
#[test]
fn test_init_creates_manifest() {
    let tmp = tempfile::tempdir().unwrap();
    let path = tmp.path().to_str().unwrap();

    betlangiser::manifest::init_manifest(path).unwrap();

    let manifest_path = tmp.path().join("betlangiser.toml");
    assert!(manifest_path.exists(), "betlangiser.toml should be created");

    // The created manifest should parse and validate successfully.
    let m = load_manifest(manifest_path.to_str().unwrap()).unwrap();
    assert!(validate(&m).is_ok());
    assert_eq!(m.project.name, "my-model");
    assert!(!m.variables.is_empty());
}

/// Test that `init_manifest` refuses to overwrite an existing file.
#[test]
fn test_init_refuses_overwrite() {
    let tmp = tempfile::tempdir().unwrap();
    let path = tmp.path().to_str().unwrap();

    // First init succeeds.
    betlangiser::manifest::init_manifest(path).unwrap();
    // Second init should fail.
    assert!(betlangiser::manifest::init_manifest(path).is_err());
}

