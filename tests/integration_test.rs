/// Test that `generate_betlang_code` produces valid Betlang source
/// with distribution declarations, propagation rules, and an entry point.
#[test]
fn test_generate_produces_betlang_code() {
    let m = make_risk_model_manifest();
    let code = generate_betlang_code(&m).unwrap();

    // Header and module declaration.
    assert!(
        code.contains("Generated by betlangiser"),
        "Should contain generation header"
    );
    assert!(
        code.contains("module risk_model where"),
        "Should contain module declaration"
    );

    // Distribution declarations for all three variables.
    assert!(
        code.contains("let price : Prob<Float> = Normal(100, 5)"),
        "Should declare price as Normal"
    );
    assert!(
        code.contains("let demand : Prob<Float> = Uniform(50, 150)"),
        "Should declare demand as Uniform"
    );
    assert!(
        code.contains("let will_buy : Prob<Ternary> = Bernoulli(0.7)"),
        "Should declare will_buy as Bernoulli"
    );

    // Propagation rules.
    assert!(
        code.contains("@propagation(price, \"linear-error-propagation\")"),
        "Normal variables use linear error propagation"
    );
    assert!(
        code.contains("@propagation(demand, \"interval-arithmetic\")"),
        "Uniform variables use interval arithmetic"
    );
    assert!(
        code.contains("@propagation(will_buy, \"kleene-ternary\")"),
        "Bernoulli variables use Kleene ternary logic"
    );

    // Ternary helpers (present because of Bernoulli variable).
    assert!(
        code.contains("let ternary_and"),
        "Should include ternary AND"
    );
    assert!(code.contains("let ternary_or"), "Should include ternary OR");
    assert!(
        code.contains("let ternary_not"),
        "Should include ternary NOT"
    );
    assert!(
        code.contains("let will_buy_ternary"),
        "Should include per-variable ternary evaluator"
    );

    // Simulation config.
    assert!(code.contains("@config(samples = 10000)"));
    assert!(code.contains("@config(confidence = 0.95)"));
    assert!(code.contains("@config(seed = 42)"));

    // Entry point.
    assert!(code.contains("let simulate()"));
}

