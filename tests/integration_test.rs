/// Test the full pipeline: write manifest to disk, load, validate, generate.
#[test]
fn test_end_to_end_generate() {
    let tmp = tempfile::tempdir().unwrap();
    let manifest_path = tmp.path().join("betlangiser.toml");
    let output_dir = tmp.path().join("generated");

    // Write a manifest to disk.
    std::fs::write(
        &manifest_path,
        r#"
[project]
name = "e2e-test"

[[variables]]
name = "temperature"
distribution = "normal"
mean = 20.0
std-dev = 3.0

[[variables]]
name = "sensor_ok"
distribution = "bernoulli"
probability = 0.95

[simulation]
samples = 5000
confidence = 0.99
output-format = "json"
"#,
    )
    .unwrap();

    // Load and validate.
    let m = load_manifest(manifest_path.to_str().unwrap()).unwrap();
    assert!(validate(&m).is_ok());
    assert_eq!(m.variables.len(), 2);

    // Generate code.
    betlangiser::generate(
        manifest_path.to_str().unwrap(),
        output_dir.to_str().unwrap(),
    )
    .unwrap();

    // Check that the output file exists.
    let output_file = output_dir.join("e2e_test.bet");
    assert!(
        output_file.exists(),
        "Generated Betlang file should exist at {}",
        output_file.display()
    );

    // Read and verify generated code.
    let code = std::fs::read_to_string(&output_file).unwrap();
    assert!(code.contains("let temperature : Prob<Float>"));
    assert!(code.contains("let sensor_ok : Prob<Ternary>"));
    assert!(code.contains("@config(samples = 5000)"));
    assert!(code.contains("@config(confidence = 0.99)"));
    assert!(code.contains("@config(output = \"json\")"));
}

