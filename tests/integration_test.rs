/// Test that Normal distribution parameters are correctly validated.
#[test]
fn test_normal_distribution_params() {
    // Valid construction.
    let d = Distribution::new_normal(100.0, 5.0).unwrap();
    assert_eq!(d.kind(), "normal");
    match d {
        Distribution::Normal { mean, std_dev } => {
            assert_eq!(mean, 100.0);
            assert_eq!(std_dev, 5.0);
        }
        _ => panic!("Expected Normal variant"),
    }

    // Invalid: zero std_dev.
    assert!(
        Distribution::new_normal(0.0, 0.0).is_err(),
        "Zero std_dev should be rejected"
    );

    // Invalid: negative std_dev.
    assert!(
        Distribution::new_normal(0.0, -1.0).is_err(),
        "Negative std_dev should be rejected"
    );
}

/// Test that all distribution types can be constructed with valid parameters.
#[test]
fn test_all_distributions_valid() {
    let normal = Distribution::new_normal(0.0, 1.0);
    assert!(normal.is_ok(), "Normal(0, 1) should be valid");

    let uniform = Distribution::new_uniform(0.0, 10.0);
    assert!(uniform.is_ok(), "Uniform(0, 10) should be valid");

    let beta = Distribution::new_beta(2.0, 5.0);
    assert!(beta.is_ok(), "Beta(2, 5) should be valid");

    let bernoulli = Distribution::new_bernoulli(0.7);
    assert!(bernoulli.is_ok(), "Bernoulli(0.7) should be valid");

    let custom = Distribution::new_custom("mixture(0.5, normal(0,1), normal(5,2))".to_string());
    assert!(custom.is_ok(), "Custom with expression should be valid");

    // Verify kind() for each.
    assert_eq!(normal.unwrap().kind(), "normal");
    assert_eq!(uniform.unwrap().kind(), "uniform");
    assert_eq!(beta.unwrap().kind(), "beta");
    assert_eq!(bernoulli.unwrap().kind(), "bernoulli");
    assert_eq!(custom.unwrap().kind(), "custom");
}

// ---------------------------------------------------------------------------
// Ternary logic tests (Kleene algebra)
// ---------------------------------------------------------------------------

/// Test Kleene strong three-valued AND — complete truth table.
#[test]
fn test_ternary_logic_and() {
    use TernaryBool::*;

    // True AND x
    assert_eq!(True.and(True), True);
    assert_eq!(True.and(False), False);
    assert_eq!(True.and(Unknown), Unknown);

    // False AND x (absorbing element)
    assert_eq!(False.and(True), False);
    assert_eq!(False.and(False), False);
    assert_eq!(False.and(Unknown), False);

    // Unknown AND x
    assert_eq!(Unknown.and(True), Unknown);
    assert_eq!(Unknown.and(False), False);
    assert_eq!(Unknown.and(Unknown), Unknown);
}

/// Test Kleene strong three-valued OR — complete truth table.
#[test]
fn test_ternary_logic_or() {
    use TernaryBool::*;

    // True OR x (absorbing element)
    assert_eq!(True.or(True), True);
    assert_eq!(True.or(False), True);
    assert_eq!(True.or(Unknown), True);

    // False OR x
    assert_eq!(False.or(True), True);
    assert_eq!(False.or(False), False);
    assert_eq!(False.or(Unknown), Unknown);

    // Unknown OR x
    assert_eq!(Unknown.or(True), True);
    assert_eq!(Unknown.or(False), Unknown);
    assert_eq!(Unknown.or(Unknown), Unknown);
}

/// Test Kleene strong three-valued NOT.
#[test]
fn test_ternary_logic_not() {
    assert_eq!(TernaryBool::True.not(), TernaryBool::False);
    assert_eq!(TernaryBool::False.not(), TernaryBool::True);
    assert_eq!(TernaryBool::Unknown.not(), TernaryBool::Unknown);
}

/// Test De Morgan's laws hold for Kleene ternary logic.
#[test]
fn test_ternary_de_morgan() {
    let values = [TernaryBool::True, TernaryBool::False, TernaryBool::Unknown];
    for &a in &values {
        for &b in &values {
            // NOT (A AND B) == (NOT A) OR (NOT B)
            assert_eq!(a.and(b).not(), a.not().or(b.not()));
            // NOT (A OR B) == (NOT A) AND (NOT B)
            assert_eq!(a.or(b).not(), a.not().and(b.not()));
        }
    }
}

